package com.yedam.MusicList;

public class Music {

	private String musicName;
	private String musicSinger;
	private String musicComposer;
	private String musicLyricist;
	private String musicDate;
	private String musicGame;
	
	
	
	

	public String getMusicName() {
		return musicName;
	}
	public void setMusicName(String musicName) {
		this.musicName = musicName;
	}
	public String getMusicSinger() {
		return musicSinger;
	}
	public void setMusicSinger(String musicSinger) {
		this.musicSinger = musicSinger;
	}
	public String getMusicComposer() {
		return musicComposer;
	}
	public void setMusicComposer(String musicComposer) {
		this.musicComposer = musicComposer;
	}
	
	public String getMusicLyricist() {
		return musicLyricist;
	}
	public void setMusicLyricist(String musicLyricist) {
		this.musicLyricist = musicLyricist;
	}
	
	public String getMusicDate() {
		return musicDate;
	}
	public void setMusicDate(String musicDate) {
		this.musicDate = musicDate;
	}
	
	public String getMusicGame() {
		return musicGame;
	}
	public void setMusicGame(String musicGame) {
		this.musicGame = musicGame;
	}

}
